import Head from "next/head";
import styles from "@/styles/Home.module.scss";
import CardSport from "@/components/cardSport";
import { getCookie, setCookie } from "cookies-next";
import { useRouter } from "next/router";
import { useEffect } from "react";

export default function Home({ username }) {
  const router = useRouter();
  const handleCardClick = (title) => {
    setCookie("selectedSport", title);
  };

  useEffect(() => {
    if (!username) {
      router.push("/signIn");
    }
  }, [username]);

  return (
    <>
      <Head>
        <title>JustPlay App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.png" />
      </Head>
      <main className={styles.main}>
        <div className={styles.header}>
          {username ? (
            <>
              <h3>JustPlay: gioca o organizza partite</h3>
              <p className={styles.paragraph}>
                Trova e partecipa a partite vicino a te o organizza la tua.
                Clicca sullo sport di tuo interesse per connetterti con
                appassionati e scoprire nuove sfide nella tua citt√†
              </p>
              <div className={styles.CardSports}>
                <CardSport
                  image="./Soccer.jpg"
                  title="Calcio"
                  onClick={handleCardClick}
                />
                <CardSport
                  image="./Tennis.jpg"
                  title="Tennis"
                  onClick={handleCardClick}
                />
                <CardSport
                  image="./Volley.jpg"
                  title="Pallavolo"
                  onClick={handleCardClick}
                />
                <CardSport
                  image="./Basket.jpg"
                  title="Basket"
                  onClick={handleCardClick}
                />
              </div>
            </>
          ) : null}
        </div>
      </main>
    </>
  );
}

export async function getServerSideProps(context) {
  const req = context.req;
  const res = context.res;
  let username = getCookie("username", { req, res });
  if (username === undefined) {
    username = false;
  }
  return { props: { username } };
}
